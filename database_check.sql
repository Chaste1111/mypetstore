-- MyPetStore 数据库检查和初始化脚本

-- 1. 创建数据库（如果不存在）
CREATE DATABASE IF NOT EXISTS mypetstore 
DEFAULT CHARACTER SET utf8mb4 
DEFAULT COLLATE utf8mb4_unicode_ci;

USE mypetstore;

-- 2. 检查并创建分类表
CREATE TABLE IF NOT EXISTS CATEGORY (
    CATID VARCHAR(80) PRIMARY KEY,
    NAME VARCHAR(80) NOT NULL,
    DESCN VARCHAR(255)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- 3. 检查并创建产品表
CREATE TABLE IF NOT EXISTS PRODUCT (
    PRODUCTID VARCHAR(80) PRIMARY KEY,
    CATEGORY VARCHAR(80) NOT NULL,
    NAME VARCHAR(80) NOT NULL,
    `DESC` VARCHAR(255),
    FOREIGN KEY (CATEGORY) REFERENCES CATEGORY(CATID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- 4. 检查并创建商品表
CREATE TABLE IF NOT EXISTS ITEM (
    ITEMID VARCHAR(80) PRIMARY KEY,
    PRODUCTID VARCHAR(80) NOT NULL,
    LISTPRICE DECIMAL(10,2) NOT NULL,
    UNITCOST DECIMAL(10,2) NOT NULL,
    SUPPLIER VARCHAR(80),
    STATUS VARCHAR(2) DEFAULT 'P',
    ATTR1 VARCHAR(80),
    ATTR2 VARCHAR(80),
    ATTR3 VARCHAR(80),
    ATTR4 VARCHAR(80),
    ATTR5 VARCHAR(80),
    FOREIGN KEY (PRODUCTID) REFERENCES PRODUCT(PRODUCTID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- 5. 检查并创建库存表
CREATE TABLE IF NOT EXISTS INVENTORY (
    ITEMID VARCHAR(80) PRIMARY KEY,
    QTY INT NOT NULL DEFAULT 0,
    FOREIGN KEY (ITEMID) REFERENCES ITEM(ITEMID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

